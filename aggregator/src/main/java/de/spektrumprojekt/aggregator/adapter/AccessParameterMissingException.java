package de.spektrumprojekt.aggregator.adapter;

/**
 * Exception to be thrown by an adapter to indicate that a required parameter is missing
 * 
 * @author Communote GmbH - <a href="http://www.communote.de/">http://www.communote.com/</a>
 * 
 */
public class AccessParameterMissingException extends AccessParameterValidationException {
    /**
     * default serial version UID
     */
    private static final long serialVersionUID = 1L;

    public AccessParameterMissingException(String parameterKey) {
        super(parameterKey, "The parameter " + parameterKey + " is required");
    }
}
